// Update the category page to handle search queries
function handleSearchInCategory() {
    const urlParams = new URLSearchParams(window.location.search);
    const searchQuery = urlParams.get('search');
    
    if (searchQuery) {
        const results = articles.filter(article => 
            article.title.toLowerCase().includes(searchQuery.toLowerCase()) ||
            article.excerpt.toLowerCase().includes(searchQuery.toLowerCase()) ||
            article.content.toLowerCase().includes(searchQuery.toLowerCase()) ||
            article.category.toLowerCase().includes(searchQuery.toLowerCase()) ||
            article.tags.some(tag => tag.toLowerCase().includes(searchQuery.toLowerCase()))
        );
        
        document.getElementById('categoryTitle').textContent = 'Search Results';
        document.getElementById('categoryDescription').textContent = `${results.length} results found for "${searchQuery}"`;
        
        const container = document.getElementById('categoryArticles');
        container.innerHTML = '';
        
        if (results.length > 0) {
            results.forEach(article => {
                container.appendChild(createArticleCard(article));
            });
        } else {
            container.innerHTML = `
                <div style="text-align: center; padding: 3rem; grid-column: 1 / -1;">
                    <h2 style="font-size: 1.5rem; margin-bottom: 1rem;">No Results Found</h2>
                    <p style="color: #6b7280;">We couldn't find any articles matching your search query. Please try different keywords.</p>
                </div>
            `;
        }
        return true;
    }
    return false;
}

