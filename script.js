// Enhanced search functionality with live results
function showSearchResults(query) {
    if (!query || query.length < 2) {
        hideSearchResults();
        return;
    }

    const results = articles.filter(article => 
        article.title.toLowerCase().includes(query.toLowerCase()) ||
        article.excerpt.toLowerCase().includes(query.toLowerCase()) ||
        article.content.toLowerCase().includes(query.toLowerCase()) ||
        article.category.toLowerCase().includes(query.toLowerCase()) ||
        article.tags.some(tag => tag.toLowerCase().includes(query.toLowerCase()))
    ).slice(0, 5); // Limit to 5 results

    displaySearchDropdown(results, query);
}

function displaySearchDropdown(results, query) {
    const searchResults = document.getElementById('searchResults');
    const mobileSearchResults = document.getElementById('mobileSearchResults');
    
    let resultsHTML = '';
    
    if (results.length > 0) {
        results.forEach(article => {
            resultsHTML += `
                <div class="search-result-item" onclick="navigateToArticle('${article.id}')">
                    <div class="search-result-category">${article.category}</div>
                    <div class="search-result-title">${highlightText(article.title, query)}</div>
                    <div class="search-result-excerpt">${highlightText(article.excerpt.substring(0, 100) + '...', query)}</div>
                </div>
            `;
        });
    } else {
        resultsHTML = `
            <div class="search-result-item">
                <div class="search-result-title">No results found</div>
                <div class="search-result-excerpt">Try different keywords</div>
            </div>
        `;
    }
    
    if (searchResults) {
        searchResults.innerHTML = resultsHTML;
        searchResults.classList.remove('hidden');
    }
    
    if (mobileSearchResults) {
        mobileSearchResults.innerHTML = resultsHTML;
        mobileSearchResults.classList.remove('hidden');
    }
}

function hideSearchResults() {
    const searchResults = document.getElementById('searchResults');
    const mobileSearchResults = document.getElementById('mobileSearchResults');
    
    if (searchResults) {
        searchResults.classList.add('hidden');
    }
    
    if (mobileSearchResults) {
        mobileSearchResults.classList.add('hidden');
    }
}

function highlightText(text, query) {
    if (!query) return text;
    const regex = new RegExp(`(${query})`, 'gi');
    return text.replace(regex, '<strong>$1</strong>');
}

