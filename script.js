// Navigation functions
function showPage(page) {
    hideAllPages();
    currentPage = page;
    
    if (page === 'home') {
        document.getElementById('homePage').classList.remove('hidden');
        loadHomePage();
    } else if (page === 'about') {
        document.getElementById('aboutPage').classList.remove('hidden');
    } else if (page === 'contact') {
        document.getElementById('contactPage').classList.remove('hidden');
    }
    
    updateActiveNavLinks();
    if (mobileMenuOpen) {
        toggleMobileMenu();
    }
}

function showCategory(categorySlug) {
    hideAllPages();
    currentPage = 'category';
    currentCategory = categorySlug;
    
    document.getElementById('categoryPage').classList.remove('hidden');
    loadCategoryPage(categorySlug);
    updateActiveNavLinks();
    
    if (mobileMenuOpen) {
        toggleMobileMenu();
    }
}

function hideAllPages() {
    const pages = document.querySelectorAll('.page');
    pages.forEach(page => page.classList.add('hidden'));
}

function updateActiveNavLinks() {
    // Remove active class from all links
    const navLinks = document.querySelectorAll('.nav-link, .mobile-nav-link');
    navLinks.forEach(link => link.classList.remove('active'));
    
    // Add active class to current page/category links
    if (currentPage === 'home') {
        document.querySelectorAll('.home-link').forEach(link => link.classList.add('active'));
    }
}

// Mobile menu toggle
function toggleMobileMenu() {
    const mobileMenu = document.querySelector('.mobile-menu');
    const menuIcon = document.querySelector('.menu-icon');
    const closeIcon = document.querySelector('.close-icon');
    
    mobileMenuOpen = !mobileMenuOpen;
    
    if (mobileMenuOpen) {
        mobileMenu.classList.remove('hidden');
        menuIcon.classList.add('hidden');
        closeIcon.classList.remove('hidden');
    } else {
        mobileMenu.classList.add('hidden');
        menuIcon.classList.remove('hidden');
        closeIcon.classList.add('hidden');
    }
}

// Bot functionality
function toggleBot() {
    const botChat = document.getElementById('botChat');
    const botToggle = document.getElementById('botToggle');
    const botIcon = botToggle.querySelector('.bot-icon');
    const closeIcon = botToggle.querySelector('.close-icon');
    
    botOpen = !botOpen;
    
    if (botOpen) {
        botChat.classList.remove('hidden');
        botIcon.classList.add('hidden');
        closeIcon.classList.remove('hidden');
    } else {
        botChat.classList.add('hidden');
        botIcon.classList.remove('hidden');
        closeIcon.classList.add('hidden');
        clearBotResponse();
    }
}

