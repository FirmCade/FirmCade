// Enhanced category loading with tag support
function loadCategoryPage(categorySlug) {
    const urlParams = new URLSearchParams(window.location.search);
    const tag = urlParams.get('tag');
    
    let categoryTitle = '';
    let categoryDescription = '';
    let categoryArticles = [];
    
    if (tag) {
        // Load articles by tag
        categoryTitle = `#${tag}`;
        categoryDescription = `Articles tagged with #${tag}`;
        categoryArticles = articles.filter(article => 
            article.tags.some(articleTag => articleTag.toLowerCase() === tag.toLowerCase())
        );
    } else if (categorySlug === 'latest') {
        categoryTitle = 'Latest News';
        categoryDescription = 'Stay updated with the most recent news and developments.';
        categoryArticles = articles.slice().sort((a, b) => new Date(b.date) - new Date(a.date));
    } else {
        const categoryMap = {
            'technology': 'Technology',
            'business': 'Business', 
            'startups': 'Startups',
            'ai': 'AI',
            'engines': 'Engines'
        };
        
        categoryTitle = categoryMap[categorySlug] || 'Category';
        categoryDescription = `Explore our latest articles and insights about ${categoryTitle.toLowerCase()}.`;
        categoryArticles = articles.filter(article => article.category.toLowerCase() === categorySlug);
    }
    
    // Update page title
    document.title = `${categoryTitle} - FirmCade`;
    
    document.getElementById('categoryTitle').textContent = categoryTitle;
    document.getElementById('categoryDescription').textContent = categoryDescription;
    
    const container = document.getElementById('categoryArticles');
    container.innerHTML = '';
    
    if (categoryArticles.length > 0) {
        categoryArticles.forEach(article => {
            container.appendChild(createArticleCard(article));
        });
    } else {
        container.innerHTML = `
            <div style="text-align: center; padding: 3rem; grid-column: 1 / -1;">
                <h2 style="font-size: 1.5rem; margin-bottom: 1rem;">No Articles Found</h2>
                <p style="color: #6b7280;">There are no articles ${tag ? 'with this tag' : 'in this category'} yet. Please check back later.</p>
            </div>
        `;
    }
}

